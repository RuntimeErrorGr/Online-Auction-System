package Employee;

public abstract class Employee {
    private String name;

    protected Employee() {}

    protected Employee(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
